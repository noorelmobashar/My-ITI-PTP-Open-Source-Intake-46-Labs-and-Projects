#include <iostream>

using namespace std;

int main()
{
    float x, y;

    cout << "Enter Value for x: "; cin >> x;
    cout << "Enter Value for y: "; cin >> y;

    cout << "x + y = " << x + y;
    cout << "\nx - y = " << x - y;
    cout << "\nx * y = " << x * y;
    cout << "\nx / y = " << x / y;

    return 0;
}
